    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Conductor con = objGestorP.buscarConductorPorId(jTextId.getText());
            if(con!=null)
            {
                jTextId.setEditable(false);
                jTextCarnet.setText(con.getIdCarnet());
                jTextFieldNombre.setText(con.getNombres()+" "+ con.getApellidos());
                jButtonReg.setEnabled(true);
            }
            else
                JOptionPane.showMessageDialog(null, "Conductor no encontrado");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUIRegistrarVeh.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    public boolean validarDatos()
    {
        
        if("".equals(jTextId.getText().toString()) || "".equals(jTextCarnet.getText()) || "".equals(jTextMarca.getText()) || "".equals(jTextPlaca.getText()))
        {
            jLabelDatosObligatorios.setForeground(Color.red);
            return false;          
        }
        
        
        
        return true;
    }
    
