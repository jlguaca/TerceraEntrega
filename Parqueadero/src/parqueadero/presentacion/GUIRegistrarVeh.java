    private void jButtonRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegActionPerformed
        jLabelDatosObligatorios.setForeground(Color.red);
        if(validarDatos()){
            
            Vehiculo objVeh = new Vehiculo(jTextId.getText(),jTextPlaca.getText(),jComboBoxTipo.getSelectedItem().toString(),jTextCarnet.getText(),jTextMarca.getText());
            if(objGestorP.agregarVehiculoAConductor(objVeh))
            {
                JOptionPane.showMessageDialog(null, "Agregado Correctamente");
                limpiarCajas(true); 
                this.setVisible(false);
            }
            else
            JOptionPane.showMessageDialog(null, "No fue posible agregar al conductor");
        }
    }//GEN-LAST:event_jButtonRegActionPerformed

