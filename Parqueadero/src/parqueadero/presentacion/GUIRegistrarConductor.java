    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
       jLabelDatosObligatorios.setForeground(Color.red);
        if(validarDatos()){
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
          
            String fecha =  sdf.format(rSDateFechaNac.getDatoFecha());
            Conductor objCon = new Conductor(jTextId.getText(),jTextIdCarnet.getText(),jTextNombres.getText(),jTextApellidos.getText(),jComboBoxRol.getSelectedItem().toString(),jComboBoxGenero.getSelectedItem().toString(),fecha);
            if(objGestorP.agregarConductor(objCon))
            {
                JOptionPane.showMessageDialog(null, "Agregado Correctamente");
                limpiarCajas(true); 
                this.setVisible(false);
            }
            else
            JOptionPane.showMessageDialog(null, "No fue posible agregar al conductor");
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

     public boolean validarDatos()
    {
        
        if("".equals(jTextId.getText().toString()) || "".equals(jTextIdCarnet.getText()) || "".equals(jTextNombres.getText()) || "".equals(jTextApellidos.getText()) || rSDateFechaNac.getDatoFecha() == null)
        {
            jLabelDatosObligatorios.setForeground(Color.red);
            return false;          
        }
        
        return true;
    }

